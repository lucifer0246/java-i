public class Comparison {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 20;

        System.out.println("where a=10, b=20, c=20");
        System.out.println("a == b is " +(a == b));
        System.out.println("b == c is " +(b == c));
        System.out.println("a != b is " +(a != b));
        System.out.println("a <= b is " +(a <= b));
        System.out.println("a >= b is " +(a >= b));
        System.out.println("a < b is " +(a < b));
        System.out.println("a > b is " +(a > b));
        System.out.println("c > b is " +(c > b));
        System.out.println("c < b is " +(c < b));

    }
}
