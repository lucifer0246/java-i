public class ExampleLoops3 {
    public static void main(String[] args) {
        for(int a=1 ; a<=10; a++ ) {
            for(int b=1; b<=10; b++) {
                System.out.println("Value of a: "+a+" and Value of b: "+b);
            }
            System.out.println("%%%%%%%%%%%%%%%%%%%%%%%%%%%");
        }
    }
}
