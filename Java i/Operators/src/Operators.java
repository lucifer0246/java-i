public class Operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        System.out.println("a + b = "+ a + b);
        System.out.println("a + b = "+(a + b));
        System.out.println("a - b = "+(a - b));
        System.out.println("a * b = "+ a * b);
        System.out.println("a / b = "+ a / b);
        System.out.println("a % b = "+ a % b);
        System.out.println("a is "+ a );
        System.out.println("a = a++ is "+ a++ );
        System.out.println("a is "+ a );
        System.out.println("a = a-- is "+ a--);
        System.out.println("a is "+ a );
        System.out.println("a = ++a is "+ ++a);
        System.out.println("a is "+ a );
        System.out.println("a = --a is "+ --a);
        System.out.println("a is "+ a );
        System.out.println("a += 1 is "+ (a+=1));
        System.out.println("a is "+ a );
        System.out.println("a -= 1 is "+ (a-=1));
        System.out.println("a is "+ a );
        System.out.println("a += 1 is "+ (a+=1));

    }
}
