public class Ternary {
    public static void main(String[] args) {
        boolean x;
        x = (5>4) ? true : false;
        System.out.println(x);

        int num1 = 10;
        int num2 = 20;

        int result = (num1>num2) ? (num1+num2) : (num1-num2);
        System.out.println(result);


    }
}
